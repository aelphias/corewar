void	dump(uint8_t *arena)
{
	int i;

	i = 0;
	while (++i <= MEM_SIZE) 
	{
		ft_printf("%02x ", arena[i - 1]);
		if (i % 32 == 0 && i != 0)   // здесь можно изменить ширину вывода
			ft_printf("\n");
	// добавить зависимость от цикла
	}
}
